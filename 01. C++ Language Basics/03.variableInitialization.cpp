#include <iostream>
using namespace std;

int main() {
  int a = 786;
  cout << "Value of a is : " << a << endl;

  char ch = 'a';
  cout << "The value of ch is : " << ch << endl;

  // we can replace true with 1 and false with zero
  bool flag = false;
  cout << "Value of flag is : " << flag << endl;

  cout << endl;
  cout << endl;
  cout << endl;
  return 0;
}
