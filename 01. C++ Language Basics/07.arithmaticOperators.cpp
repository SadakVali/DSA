#include <iostream>
using namespace std;

int main() {
  int a = 5;
  int b = 3;

  cout << a + b << endl;
  cout << a - b << endl;
  cout << a * b << endl;
  cout << a % b << endl; // remainder operator
  // int / int => int, float / int => float , double / int => double, 
  // int / float => ?, float / double => ?, double / float => ?
  cout << a / b << endl; 
  
  cout << endl;
  cout << endl;
  cout << endl;
  return 0;
}